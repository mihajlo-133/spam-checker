def main():
    parser = argparse.ArgumentParser(
        description="Check email copy for spam trigger words (772 patterns, 5 categories)",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""Examples:
  echo "Click here for a free offer!" | python spam_checker.py
  python spam_checker.py --text "Act now, limited time only!"
  python spam_checker.py --file email.txt --json
  python spam_checker.py --dir campaigns/""",
    )
    source = parser.add_mutually_exclusive_group()
    source.add_argument("--text", "-t", help="Email text to check")
    source.add_argument("--file", "-f", help="File containing email text")
    source.add_argument("--dir", "-d", help="Directory of .txt/.md files to batch check")
    parser.add_argument("--json", "-j", action="store_true", help="Output as JSON")
    parser.add_argument("--quiet", "-q", action="store_true", help="Only show flagged words, no header")

    args = parser.parse_args()

    # Determine input
    if args.dir:
        # Batch mode
        p = Path(args.dir)
        files = sorted(p.glob("*.txt")) + sorted(p.glob("*.md"))
        if not files:
            print(f"No .txt or .md files found in {args.dir}")
            sys.exit(1)

        results = []
        for f in files:
            text = f.read_text(encoding="utf-8", errors="replace")
            result = check_spam(text)
            result["file"] = str(f)
            results.append(result)

        if args.json:
            batch_output = []
            for r in results:
                batch_output.append({
                    "file": r["file"],
                    "score": r["score"],
                    "rating": r["rating"],
                    "total_hits": r["total_hits"],
                    "categories": {
                        cat: [item["keyword"] for item in items]
                        for cat, items in r["categories"].items()
                    },
                })
            print(json.dumps(batch_output, indent=2))
        else:
            # Summary table
            print(f"\n{'File':<40} {'Rating':<8} {'Hits':<6} {'Categories'}")
            print("─" * 80)
            for r in results:
                cats = ", ".join(f"{cat}({len(items)})" for cat, items in r["categories"].items())
                color = {"Great": "\033[32m", "Okay": "\033[33m", "Poor": "\033[31m"}
                reset = "\033[0m"
                fname = Path(r["file"]).name
                if len(fname) > 38:
                    fname = fname[:35] + "..."
                print(f"  {fname:<38} {color.get(r['rating'], '')}{r['rating']:<8}{reset} {r['total_hits']:<6} {cats}")
            print()
        sys.exit(0)

    if args.text:
        text = args.text
    elif args.file:
        text = Path(args.file).read_text(encoding="utf-8", errors="replace")
    elif not sys.stdin.isatty():
        text = sys.stdin.read()
    else:
        parser.print_help()
        sys.exit(1)

    result = check_spam(text)

    if args.json:
        print(format_json(result))
    elif args.quiet:
        for item in result["flagged_words"]:
            print(f"{item['category']}: {item['keyword']}")
    else:
        print(format_human(result, text))


if __name__ == "__main__":
    main()
