# ─── Scoring engine ─────────────────────────────────────────────────

CATEGORY_ICONS = {
    "urgency": "🚨",
    "shady": "🔞",
    "overpromise": "🤩",
    "money": "💰",
    "unnatural": "💬",
}


def check_spam(text: str) -> dict:
    """
    Run all spam patterns against text. Returns a dict with:
      - score: int
      - rating: "Great" | "Okay" | "Poor"
      - total_hits: int
      - word_count: int
      - categories: {category: [{"keyword": str, "match": str}]}
      - flagged_words: [{"keyword": str, "category": str, "match": str}]
    """
    words = text.split()
    word_count = len(words)

    categories: dict[str, list[dict]] = {}
    flagged: list[dict] = []
    seen: set[tuple[str, str]] = set()  # (keyword, category) dedup

    for pattern, keyword, category in SPAM_PATTERNS:
        for match in pattern.finditer(text):
            key = (keyword, category)
            if key not in seen:
                seen.add(key)
                entry = {"keyword": keyword, "category": category, "match": match.group()}
                flagged.append(entry)
                categories.setdefault(category, []).append(entry)

    total_hits = len(flagged)

    # Scoring — exact Mailmeteor algorithm
    score = total_hits
    if "money" in categories or "shady" in categories:
        score += 20
    if "urgency" in categories or "overpromise" in categories:
        score += 10

    if score > 20:
        rating = "Poor"
    elif score > 5:
        rating = "Okay"
    else:
        rating = "Great"

    return {
        "score": score,
        "rating": rating,
        "total_hits": total_hits,
        "word_count": word_count,
        "categories": categories,
        "flagged_words": flagged,
    }


# ─── Output formatters ──────────────────────────────────────────────

def format_human(result: dict, text: str) -> str:
    """Format results for terminal display."""
    lines = []

    # Score header
    rating = result["rating"]
    color = {"Great": "\033[32m", "Okay": "\033[33m", "Poor": "\033[31m"}
    reset = "\033[0m"
    lines.append(f"\n{'─' * 50}")
    lines.append(f"  Score: {color.get(rating, '')}{rating}{reset}  ({result['total_hits']} spam triggers found)")
    lines.append(f"  Words: {result['word_count']}")
    lines.append(f"{'─' * 50}")

    if not result["flagged_words"]:
        lines.append("  ✅ No spam trigger words detected!")
        return "\n".join(lines)

    # Category breakdown
    for cat in ["urgency", "shady", "overpromise", "money", "unnatural"]:
        if cat not in result["categories"]:
            continue
        items = result["categories"][cat]
        icon = CATEGORY_ICONS[cat]
        lines.append(f"\n  {icon} {cat.title()} ({len(items)})")
        for item in sorted(items, key=lambda x: x["keyword"].lower()):
            lines.append(f"     • {item['keyword']}")

    lines.append("")
    return "\n".join(lines)


def format_json(result: dict) -> str:
    """Format results as JSON."""
    output = {
        "score": result["score"],
        "rating": result["rating"],
        "total_hits": result["total_hits"],
        "word_count": result["word_count"],
        "categories": {
            cat: [item["keyword"] for item in items]
            for cat, items in result["categories"].items()
        },
    }
    return json.dumps(output, indent=2)


